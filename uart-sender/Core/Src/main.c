typedef struct {
    Accel_xyz buffer[WINDOW_SIZE]; // Circular buffer for storing data
    int count;                     // Number of elements currently in the buffer
    int index;                     // Current index in the circular buffer
    Accel_xyz sum;                 // Sum of elements in the window
} SlidingWindow;

// Initialize the sliding window
void SlidingWindow_Init(SlidingWindow *window) {
    memset(window, 0, sizeof(SlidingWindow));
}

// Add a new element to the sliding window and calculate the running average
Accel_xyz SlidingWindow_Add(SlidingWindow *window, Accel_xyz new_data) {
    // Remove the oldest data from the sum if the window is full
    if (window->count == WINDOW_SIZE) {
        Accel_xyz oldest = window->buffer[window->index];
        window->sum.AccelX_g -= oldest.AccelX_g;
        window->sum.AccelY_g -= oldest.AccelY_g;
        window->sum.AccelZ_g -= oldest.AccelZ_g;
    } else {
        // Increment the count if the window isn't full
        window->count++;
    }

    // Add the new data to the sum
    window->sum.AccelX_g += new_data.AccelX_g;
    window->sum.AccelY_g += new_data.AccelY_g;
    window->sum.AccelZ_g += new_data.AccelZ_g;

    // Store the new data in the circular buffer
    window->buffer[window->index] = new_data;

    // Advance the index
    window->index = (window->index + 1) % WINDOW_SIZE;

    // Calculate the running average
    Accel_xyz average = {
            .AccelX_g = window->sum.AccelX_g / window->count,
            .AccelY_g = window->sum.AccelY_g / window->count,
            .AccelZ_g = window->sum.AccelZ_g / window->count,
    };

    return average;
}
